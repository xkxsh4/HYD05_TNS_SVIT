package day2;

public class IncrementOperator {
    public static void main(String[] args) {
		int x = 10;
        System.out.println(x++); //postfix
		int y = 10;
        System.out.println(++y); //prefix
        int z = 10;
        System.out.println(z--);//postfix
        int a = 10;
        System.out.println(--a);//prefix
	}
}
